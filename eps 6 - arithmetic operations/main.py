a = 10
b = 3

# penjumlahan +
answer = a + b
print("contoh penjumlahan", a, "+", b, "=", answer )

# pengurangan -
answer = a - b
print("contoh pengurangan", a, "-", b, "=", answer )

# perkalian *
answer = a * b
print("contoh perkalian",a, "*", b, "=", answer )

# pembagian /
answer = a / b
print("contoh pembagian",a, "/", b, "=", answer )

# eksponen/pangkat **
answer = a ** b
print("contoh eksponen",a, "**", b, "=", answer )

# modulus % ==> modulus adalah sisa bagi
answer = a % b
print("contoh modulus",a, "%", b, "=", answer )

#floor division //
answer = a // b
print("contoh floor division",a, "//", b, "=", answer )


